    private void PacienteNomeFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_PacienteNomeFocusGained
        if (PacienteNome.getText().equals("Digite o nome do paciente...")) {
        PacienteNome.setText("");
        PacienteNome.setForeground(java.awt.Color.BLACK); 
        }
    }//GEN-LAST:event_PacienteNomeFocusGained

    private void PacienteNomeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_PacienteNomeFocusLost
                                        
        if (PacienteNome.getText().trim().isEmpty()) {
            PacienteNome.setText("Digite o nome do paciente...");
            PacienteNome.setForeground(java.awt.Color.GRAY); 
        }
    }//GEN-LAST:event_PacienteNomeFocusLost

    private void AlergiasFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_AlergiasFocusGained
        if (Alergias.getText().equals("Digite as Alergias do Paciente se tiver")) {
        Alergias.setText("");
        Alergias.setForeground(java.awt.Color.BLACK); 
        }
    }//GEN-LAST:event_AlergiasFocusGained

    private void AlergiasFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_AlergiasFocusLost
        if (Alergias.getText().trim().isEmpty()) {
            Alergias.setText("Digite as Alergias do Paciente se tiver");
            Alergias.setForeground(java.awt.Color.GRAY); 
        }
    }//GEN-LAST:event_AlergiasFocusLost

    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed
        
        String nome = PacienteNome.getText();
        String cpf = CPFPaciente.getText();
        String dataNasc = DataNascimento.getText();
        String tipoSangue = TipoSanguineo.getSelectedItem().toString();
        String alergias = Alergias.getText();
        
        boolean cadastro = com.artur.clinica.Controller.ClinicativaController.processarCadastroPaciente(this, nome, cpf, dataNasc, tipoSangue, alergias);
        
        if(cadastro){
            limparCampos();
        }

        
    }//GEN-LAST:event_SalvarActionPerformed
    
    private void limparCampos(){
        PacienteNome.setText("");
        CPFPaciente.setValue(null);
        TipoSanguineo.setSelectedIndex(0);
        DataNascimento.setValue(null);
        Alergias.setText("");
        
        PacienteNome.setText("Digite o nome do paciente...");
        PacienteNome.setForeground(java.awt.Color.GRAY);
        
        Alergias.setText("Digite as Alergias do Paciente se tiver");
        Alergias.setForeground(java.awt.Color.GRAY); 
    }

